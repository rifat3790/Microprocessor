INCLUDE 'EMU8086.INC'

.MODEL SMALL
.STACK 100H
.DATA
NUM DW ?
FACT DW 1

.CODE  

FACTORIAL MACRO
    MOV CX, NUM
    MOV AX, 1
FACT_LOOP:
    MUL CX
    LOOP FACT_LOOP
    MOV FACT, AX
ENDM

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX 
    
    PRINT 'ENTER A NUMBER: '
    CALL INPUT_NUMBER
    FACTORIAL

    PRINTN ''
    PRINT 'FACTORIAL: '
    MOV AX, FACT
    CALL PRINT_NUM

RETURN:
    MOV AH, 4CH
    INT 21H
MAIN ENDP

INPUT_NUMBER PROC
    CALL SCAN_NUM
    MOV NUM, CX
    RET
INPUT_NUMBER ENDP

DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
DEFINE_SCAN_NUM

END MAIN