NEWLINE MACRO
    MOV AH, 2
    MOV DL, 13
    INT 21H
    MOV DL, 10
    INT 21H
NEWLINE ENDM

ADDI MACRO NUM1, NUM2
    MOV AX, NUM1
    MOV BX, NUM2
    ADD AX, BX
ENDM

SUBTRACT MACRO NUM1, NUM2
    MOV AX, NUM1
    MOV BX, NUM2
    SUB AX, BX
ENDM

PRINT MACRO STR
    MOV DX, OFFSET STR
    MOV AH, 9
    INT 21H
PRINT ENDM

.MODEL SMALL
.STACK 100H

.DATA
    NUM1 DW ?
    NUM2 DW ?
    STR1 DW "RESULT: $"
    STR2 DW "ENTER YOUR NUMBER: $"
    ADD_RESULT DW ?
    SUB_RESULT DW ?

.CODE 
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    PRINT STR2
    NEWLINE
    MOV AH, 1
    INT 21H
    SUB AL, 30H
    MOV NUM1, AX

    PRINT STR2
    NEWLINE
    MOV AH, 1
    INT 21H
    SUB AL, 30H
    MOV NUM2, AX

    ADDI NUM1, NUM2
    MOV ADD_RESULT, AX

    SUBTRACT NUM1, NUM2
    MOV SUB_RESULT, AX

    PRINT STR1
    NEWLINE
    MOV AX, ADD_RESULT
    CALL PRINT_NUM

    PRINT STR1
    NEWLINE
    MOV AX, SUB_RESULT
    CALL PRINT_NUM

    MOV AH, 4CH
    INT 21H

MAIN ENDP

PRINT_NUM PROC
    MOV DX, 0
    MOV DL, AL
    ADD DL, 30H
    MOV AH, 2
    INT 21H
    RET
PRINT_NUM ENDP

END MAIN
